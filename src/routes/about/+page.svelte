<p>
  John's Campaign is named after Dr John Gerrard, who died in November 2014
  after a catastrophic stay in hospital. His story - the story of how this
  campaign came to be - is told eloquently by his daughter Nicci.
  <ExternalLink
    href="https://www.theguardian.com/society/2014/nov/29/nicci-gerrard-father-dementia-hospital-care-elderly">
    Her article sparked our campaign.</ExternalLink>
</p>
<p>
  Dementia is incurable and terminal yet its progression is not linear. John
  Gerrard was ‘living well’ with Alzheimers before he was cut off from his
  family, his normal routine and his familiar surroundings. Delirium (acute
  confusion) is extraordinarily dangerous for people who are already living with
  dementia. Families bring reassurance and familiarity (the clue is in the
  word!). They are also quick to notice when things are not right, even when a
  person cannot communicate for themselves.
</p>
<p>
  John's Campaign has a single, simple message: no one should enforce
  disconnection between family carers and those who need their expert knowledge
  and their love. This principle is applicable everywhere: in the doctor's
  surgery, the A&E ward, the dementia assessment unit and the care home.
</p>
<figure class="right">
  <Image file="june-jones-letter.JPG" />
  <figcaption>
    June wrote this letter early in the campaign.
    <YouTubeVideoLink id="L5va78plvIU">
      Click here for a video in which Julia reads it with her
    </YouTubeVideoLink>
  </figcaption>
</figure>
<p>
  Families are never “visitors” to a person with dementia; they are an integral
  part of that person's life and identity and often their last, best means of
  connection with the world.
</p>
<p>
  When June Jones (Julia's mother) lived in extra care accommodation and then in
  a care home, Julia was always able to act as her primary carer. June was one
  of the first supporters of John's Campaign and wrote a letter expressing her
  fear of separation and her need for loving support.
</p>
<p>
  As the illness progressed June's world shrank to the dementia nursing suite
  where she lived her last years. That care home was the first in England to
  make a John's Campaign pledge. The mutually supportive partnership between
  June's family and the professional staff helped her through physical
  deterioration and mental distress to reach the end of her life surrounded by
  love and the kindest care.
</p>
<p>John's Campaign is June's Campaign and must be Everyone's Campaign.</p>
<p>
  As Johns Campaign is a set of principles and attitudes rather than procedures
  and protocols, it will look different in different settings.
</p>
<h2>John's Campaign in Hospitals</h2>
<figure class="right">
  <Image file="age-uk-implementing-johns-campaign-cover.png" />
  <figcaption>
    <ExternalDocLink file="age-uk-implementing-johns-campaign.pdf">
