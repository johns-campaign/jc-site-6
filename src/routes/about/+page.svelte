  </figcaption>
</figure>
<figure class="right">
  <Image file="dementia-friendly-hospital-charter-cover.png" />
  <figcaption>
    <ExternalDocLink file="dementia-friendly-hospital-charter-2020.pdf">
      <em>The Dementia-Friendly Hospital Charter</em>, produced by the National
      Dementia Action Alliance
    </ExternalDocLink>
  </figcaption>
</figure>
<p>
  Early in our campaign we learned from the Alzheimer's Society what a risky
  place a hospital can be for someone with dementia, especially when they are
  deprived of the support of a willing family carer. These were statistics in
  their 2009 report
  <ExternalDocLink file="counting-the-cost.pdf">
    <em>
      Counting the Cost: caring for people with dementia on hospital wards</em
    ></ExternalDocLink
  >:
</p>
<ul>
  <li>
    Over <u>a quarter</u> of hospital beds in the UK are currently occupied by people
    with dementia
  </li>
  <li>
    The average stay of a person with dementia is <u>three weeks</u> but it can
    be
    <u>much longer</u> if rehabilitation is a problem or there is nowhere suitable
    to go
  </li>
  <li>
    <u>One third</u> of people with dementia who go into hospital for an
    <u>unrelated</u> condition <u>NEVER</u> return to their own homes
  </li>
  <li>
    <u>47%</u> of people with dementia who go into hospital are
    <u>physically less well when they leave</u> than when they went in
  </li>
  <li>
    <u>54%</u> of people with dementia who go into hospital are
    <u>mentally less well when they leave</u> than when they went in
  </li>
</ul>
<p>
  Since then much has been learned about the dangers of delirium and also the
  rate of physical deterioration when any older person remains in bed for even a
  few days. Families help provide the assistance as well as the motivation to
  eat and drink and mobilise. By the summer of 2018 all the acute trusts in
  England (and throughout the 4 UK nations) had made Carers Welcome pledges -
  and were seeing the benefits.
</p>
<p>
  Many, but not all, have slipped back to their former restrictive practices
  under the pressure of the pandemic. Perhaps they have forgotten that dementia
  remains a disability, there is a continuing duty to make individual provision
  for patients with this condition.
</p>
<p>
  We therefore welcome this recent publication by the Dementia Action Alliance
  and urge all hospitals to read it and remember their John's Campaign pledges.
</p>
