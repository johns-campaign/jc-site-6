<p>
  Though this was not the initial inspiration for John's Campaign we have been
  working with care homes since 2017 to make the best of family involvement and
  to support people's personal relationships through what are often the last
  years of life. We have many friends in the world of residential care and in
  February 2020 were supported by the Malnutrition Task Force to meet with our
  care home steering group and celebrate this publication of a jointly-produced
  Implementation booklet.
</p>
<p>
  This was the extension of a similar booklet Honoured Guests, produced in
  partnership with Care England. At the time of writing (November 2020) those
  days seem long gone. Care homes and their residents have suffered greatly yet,
  at the time of writing, it continues to feel shocking that so many ‘Homes’ are
  not acting as people's homes - the place where the people who matter most
  (spouses, committed partners, adult children and grandchildren) are always
  welcome.
</p>
<p>
  We are battling for change and know that there are very many (including a
  significant number of care home managers and staff) who are battling with us.
</p>
