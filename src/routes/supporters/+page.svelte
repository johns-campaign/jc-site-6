<h2>
  <AmbassadorsLink />
</h2>
<p>
  One of the inspiring features of this campaign has been the readiness of
  individual nurses, nursing directors, psychologists, patients experience
  managers and others to implement the principles of welcome in their own
  hospitals and then share their experience with others. Gillian McAuley was the
  first to suggest the title of Ambassador for people who believe that John's
  Campaign has enhanced the life of their hospital and who want to encourage
  others to do the same. There are many others without whom the campaign would
  never have moved beyond the stage of being a lovely idea. This is not a closed
  shop: if you would like to be a John's Campaign Ambassador, please do get in
  touch.
</p>
<h2>
  <ParticipantsLink />
</h2>
<p>
  Over 1500 hospitals and homes (and some others, such as ambulance trusts,
  clinical commissioning participants, domiciliary care companies, hospices and
  primary care providers) have pledged to support our principles of inclusion.
  We believe that people, particularly those with dementia, other mental
  illnesses, or otherwise in need of care, should not be separated from those
  important to them by protocols and systems that are harmful and wrong.
</p>
