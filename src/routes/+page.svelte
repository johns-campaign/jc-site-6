<p>
  We know that families come in all varieties. They're not always genetically
  related and sometimes it is a close friend who matters most. We believe that
  individuals are their own best judges who is essential for their emotional or
  practical well-being. We think that, wherever possible, they should be asked
  and their wishes respected.
</p>
<p>
  Legally <em>carers</em> are people who provide personal care, support and assistance
  to another individual who is in need of support due to disability, medical condition,
  including terminal or chronic illness, mental illness or is frail and aged.
</p>
<p>
  Our campaign can apply to anyone with a special need and is therefore in line
  with Equality legislation as well as human-kindness and common sense.
</p>
<p>
  <u>
    We believe that carers should not just be ‘allowed’ but should be welcomed.</u>
  They are the experts in the person for whom they care and best outcomes are likely
  when they work in partnership with the medical or social professionals. Often they
  have legal duties such as power of attorney or court of protections deputy. They
  can observe and advocate: communicate, remember, explain and reassure.
</p>
<p>
  However family carers often have other responsibilities.
  <u>They have a right <em>but not a duty</em> to continue to care.</u>
</p>
<p>
  John’s Campaign applies to all hospital settings (acute, community, mental
  health). We have come to realise that its principles must extend to all other
  caring institutions where people are living away from those closest to them.
</p>
<p>
  <u>Over 1500 institutions have pledged support</u> in the time since the campaign
  was founded in November 2014 by Nicci Gerrard and Julia Jones.
</p>
<p>
  The current coronavirus pandemic has tested their commitment beyond anything
  they or we could ever have imagined. Thank you to all who still honour your
  Carers Welcome pledge.
</p>
<p>
  John's Campaign is not a tick box exercise: it's an attitude of mind which
  respects the rights and choices of the person who needs care and values the
  essential input of the informal carer (or family member) as well as the
  expertise of the professional. Everyone has a part to play in this.
</p>
<h2>
  <AboutLink />
</h2>
<p>
  The campaign’s name honours Dr John Gerrard, who was a doctor and a business
  man, and diagnosed with Alzheimers in his mid seventies. It was a slow
  decline, that sped up when he was ill or upset, for ten years. Then, at the
  start of February 2014, he went into hospital. He had infected leg ulcers
  which weren’t responding to antibiotics. The hospital had a norovirus outbreak
  which meant visitors weren’t allowed at all. He was there for five weeks.
</p>
<p>
  John went in strong, mobile, smiling, able to tell stories about his past, to
  work in his garden and help with things round the house. He was able to feed
  himself, to keep clean, to have a good kind of daily life. He came out
  skeletal, immobile, incoherent, requiring 24-hour care and barely knowing
  those around him.
</p>
<p>
  His family are sure that if he had not spent that time alone, without them, he
  would not have descended into such a state of deep delirium. Having someone
  with you - someone who you love, who you know, whose face you know (be they
  your carer, your family, your friend, your lover) - helps keep you tied to
  reality, to life, to sanity.
</p>
<p>
  John died in November 2014. His story, however, is still repeated. Far too
  many people die cut off from the people who care for them. Far too many places
  have dangerously over-restrictive policies (both predating and during the
  present pandemic) preventing people from being with people who need them.
</p>
<p>
  In the wake of his death, John’s daughter, Nicci Gerrard, cofounded John’s
  Campaign with Julia Jones, whose mother, June, also lived well with dementia
  (both Alzheimer’s and vascular) for many years before her death in 2018.
  John’s Campaign is June’s Campaign, is Everyone’s Campaign, for none of us
  should be blocked from our best, most special friends, family or carers.
</p>
